// Package main —Å–æ–¥–µ—Ä–∂–∏—Ç —Ä–µ—à–µ–Ω–∏–µ –∑–∞–¥–∞—á–∏ –ø–æ —É–ø—Ä–æ—â–µ–Ω–∏—é –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –ø—É—Ç–∏ –≤ Unix-—Å—Ç–∏–ª–µ.
package main

import (
	"fmt"
	"strings"
)

// –ò—Å—Ö–æ–¥–Ω–∞—è –∑–∞–¥–∞—á–∞:
// –î–∞–Ω –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å –¥–æ —Ñ–∞–π–ª–∞ –∏–ª–∏ –ø–∞–ø–∫–∏ (Unix-style) - –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ —É–ø—Ä–æ—Å—Ç–∏—Ç—å –µ–≥–æ.
//
// –û–±–æ–∑–Ω–∞—á–µ–Ω–∏—è:
// "/" - —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å –∫–∞—Ç–∞–ª–æ–≥–æ–≤.
// ".." - –≤—ã—Ö–æ–¥ –Ω–∞ –∫–∞—Ç–∞–ª–æ–≥ –≤—ã—à–µ.
// "." - —Ç–µ–∫—É—â–∏–π –∫–∞—Ç–∞–ª–æ–≥.
//
// –§–æ—Ä–º–∞—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
// - –ü—É—Ç—å –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –∫–æ—Å–æ–π —á–µ—Ä—Ç—ã '/'.
// - –ö–∞—Ç–∞–ª–æ–≥–∏ —Ä–∞–∑–¥–µ–ª—è—é—Ç—Å—è –æ–¥–∏–Ω–∞—Ä–Ω–æ–π –∫–æ—Å–æ–π —á–µ—Ä—Ç–æ–π '/'.
// - –ü—É—Ç—å –Ω–µ –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è —Å–∏–º–≤–æ–ª–æ–º '/'.

// simplifyPath —É–ø—Ä–æ—â–∞–µ—Ç –∑–∞–¥–∞–Ω–Ω—ã–π –∞–±—Å–æ–ª—é—Ç–Ω—ã–π –ø—É—Ç—å.
//
// –ê–ª–≥–æ—Ä–∏—Ç–º –∏—Å–ø–æ–ª—å–∑—É–µ—Ç —Å—Ç–µ–∫ –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ–≥–æ –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –∫–∞–Ω–æ–Ω–∏—á–µ—Å–∫–æ–≥–æ –ø—É—Ç–∏.
// –°—Ç–µ–∫ ‚Äî –∏–¥–µ–∞–ª—å–Ω–∞—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è —ç—Ç–æ–π –∑–∞–¥–∞—á–∏, —Ç–∞–∫ –∫–∞–∫ –ø–µ—Ä–µ—Ö–æ–¥—ã "–≤–≤–µ—Ä—Ö" ("..")
// —è–≤–ª—è—é—Ç—Å—è –æ–ø–µ—Ä–∞—Ü–∏–µ–π LIFO (Last-In, First-Out), —á—Ç–æ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ø–æ–≤–µ–¥–µ–Ω–∏—é —Å—Ç–µ–∫–∞.
func simplifyPath(path string) string {
	// 1. –†–∞–∑–¥–µ–ª—è–µ–º —Å—Ç—Ä–æ–∫—É –ø–æ "/", —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã –ø—É—Ç–∏.
	// –ù–∞–ø—Ä–∏–º–µ—Ä, "/a/./b/../c/" -> ["", "a", ".", "b", "..", "c", ""]
	parts := strings.Split(path, "/")

	// 2. –ò—Å–ø–æ–ª—å–∑—É–µ–º —Å—Ä–µ–∑ (slice) –≤ –∫–∞—á–µ—Å—Ç–≤–µ —Å—Ç–µ–∫–∞ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è "—á–∏—Å—Ç—ã—Ö" –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–æ–≤ –ø—É—Ç–∏.
	var stack []string

	// 3. –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞–º –ø—É—Ç–∏.
	for _, part := range parts {
		switch part {
		case "", ".":
			// –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ (–≤–æ–∑–Ω–∏–∫–∞—é—Ç –∏–∑-–∑–∞ –¥–≤–æ–π–Ω—ã—Ö —Å–ª–µ—à–µ–π, –Ω–∞–ø—Ä–∏–º–µ—Ä, "//")
			// –∏ —Ç–æ—á–∫–∏ (—É–∫–∞–∑–∞–Ω–∏–µ –Ω–∞ —Ç–µ–∫—É—â–∏–π –∫–∞—Ç–∞–ª–æ–≥).
			continue
		case "..":
			// ".." –æ–∑–Ω–∞—á–∞–µ—Ç –ø–µ—Ä–µ—Ö–æ–¥ –Ω–∞ —É—Ä–æ–≤–µ–Ω—å –≤—ã—à–µ.
			// –î–ª—è —ç—Ç–æ–≥–æ –º—ã –ø—Ä–æ—Å—Ç–æ "–≤—ã—Ç–∞–ª–∫–∏–≤–∞–µ–º" –ø–æ—Å–ª–µ–¥–Ω–∏–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ —Å—Ç–µ–∫–∞.
			// –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —Å—Ç–µ–∫ –Ω–µ –ø—É—Å—Ç, —Ç–∞–∫ –∫–∞–∫ –Ω–µ–ª—å–∑—è –ø–æ–¥–Ω—è—Ç—å—Å—è –≤—ã—à–µ –∫–æ—Ä–Ω–µ–≤–æ–≥–æ –∫–∞—Ç–∞–ª–æ–≥–∞.
			if len(stack) > 0 {
				stack = stack[:len(stack)-1]
			}
		default:
			// –õ—é–±–æ–π –¥—Ä—É–≥–æ–π –∫–æ–º–ø–æ–Ω–µ–Ω—Ç ‚Äî —ç—Ç–æ –∏–º—è –∫–∞—Ç–∞–ª–æ–≥–∞.
			// "–í—Ç–∞–ª–∫–∏–≤–∞–µ–º" –µ–≥–æ –≤ —Å—Ç–µ–∫.
			stack = append(stack, part)
		}
	}

	// 4. –°–æ–±–∏—Ä–∞–µ–º –∏—Ç–æ–≥–æ–≤—ã–π –ø—É—Ç—å –∏–∑ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–æ–≤, –æ—Å—Ç–∞–≤—à–∏—Ö—Å—è –≤ —Å—Ç–µ–∫–µ.
	// –ú—ã —Å–æ–µ–¥–∏–Ω—è–µ–º –∏—Ö —á–µ—Ä–µ–∑ "/" –∏ –¥–æ–±–∞–≤–ª—è–µ–º –≤–µ–¥—É—â–∏–π —Å–ª–µ—à –¥–ª—è –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –ø—É—Ç–∏.
	return "/" + strings.Join(stack, "/")
}

func main() {
	testCases := map[string]string{
		"/etc/":                            "/etc",
		"/../../../../":                    "/",
		"/first/second/../../third/":       "/third",
		"/first/./second/./third":          "/first/second/third",
		"/a/./b/../../c/":                  "/c",
		"/home//foo/":                      "/home/foo",
		"/":                                "/",
		"/a/b/c":                           "/a/b/c",
	}

	fmt.Println("--- –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ simplifyPath ---")
	allPassed := true
	for path, expected := range testCases {
		result := simplifyPath(path)
		if result == expected {
			fmt.Printf("‚úÖ OK:     Input: '%-30s' -> Output: '%s'\n", path, result)
		} else {
			allPassed = false
			fmt.Printf("‚ùå ERROR:  Input: '%-30s' -> Output: '%s', Expected: '%s'\n", path, result, expected)
		}
	}
	fmt.Println("---------------------------------------")
	if allPassed {
		fmt.Println("üéâ –í—Å–µ —Ç–µ—Å—Ç—ã –ø—Ä–æ–π–¥–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ!")
	} else {
		fmt.Println("‚ùóÔ∏è –ù–µ–∫–æ—Ç–æ—Ä—ã–µ —Ç–µ—Å—Ç—ã –Ω–µ –ø—Ä–æ–π–¥–µ–Ω—ã.")
	}
}
